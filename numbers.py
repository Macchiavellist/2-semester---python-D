a = input("Enter a type of number: ")
if a == 'complex':
    x = 3 + 5j
    y = 5j
    z = -5j

    print(type(x))
    print(type(y))
    print(type(z))
if a == 'float' :
    x = 35e3
    y = 12E4
    z = -87.7e100

    print(type(x))
    print(type(y))
    print(type(z))
if a == 'int':
    x = 1
    y = 35656222554887711
    z = -3255522

    print(type(x))
    print(type(y))
    print(type(z))


